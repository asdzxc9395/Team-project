package Team.project.domain;



public class Board {
  
  private String title;
  private boolean notice;
  
  public String getTitle() {
    return title;
  }
  public void setTitle(String title) {
    this.title = title;
  }
  public boolean isNotice() {
    return notice;
  }
  public void setNotice(boolean notice) {
    this.notice = notice;
  }
}